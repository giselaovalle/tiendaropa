   private Proveedor obtenerProveedor()
   {
       String nombre,rut,tel,dir;
       nombre = TXTNombreProveedor.getText();
       rut = TXTRUTProveedor.getText();
       tel = TXTTelefonoProveedor.getText();
       dir = TXTDireccionProveedor.getText();
       System.out.println(""+nombre+rut+tel+dir);
       
       Proveedor elProveedor = new Proveedor();
       elProveedor.setIdProveedor((Integer)TXTIdProveedor.getValue());
       elProveedor.setNombreProveedor(TXTNombreProveedor.getText());
       elProveedor.setRUTProveedor(TXTRUTProveedor.getText());
       elProveedor.setTelefonoProveedor(TXTTelefonoProveedor.getText());
       elProveedor.setDireccionProveedor(TXTDireccionProveedor.getText());
       return elProveedor;
       
   }
          
 
 
