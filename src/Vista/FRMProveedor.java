//LLENAR TABLA
 public void llenarTabla()
 {
 Proveedor unProveedor = new Proveedor();
 DefaultTableModel tabla = (DefaultTableModel) TBLProveedor.getModel();
 Iterator<Proveedor> itProveedor = unProveedor.listar();
 Object[] filaProveedor = new Object[TBLProveedor.getColumnCount()];
 tabla.setRowCount(0);
     while (itProveedor.hasNext()) {         
         unProveedor = itProveedor.next();
         filaProveedor[0] = unProveedor.getIdProveedor();
         filaProveedor[1] = unProveedor.getNombreProveedor();
         filaProveedor[2] = unProveedor.getRUTProveedor();
         filaProveedor[3] = unProveedor.getTelefonoProveedor();
         filaProveedor[4] = unProveedor.getDireccionProveedor();
         tabla.addRow(filaProveedor);
                 
     }
 
 }
 
 //LLENAR TABLA CON BUSQUEDA
 private void llenarTablaConBusqueda(String busqueda)
 {
 Proveedor unProveedor = new Proveedor();
 DefaultTableModel tabla = (DefaultTableModel) TBLProveedor.getModel();
 Iterator<Proveedor> itProveedor = unProveedor.buscar(busqueda);
 Object[] filaProveedor = new Object[TBLProveedor.getColumnCount()];
 tabla.setRowCount(0);
     while (itProveedor.hasNext()) {         
         unProveedor = itProveedor.next();
         filaProveedor[0] = unProveedor.getIdProveedor();
         filaProveedor[1] = unProveedor.getNombreProveedor();
         filaProveedor[2] = unProveedor.getRUTProveedor();
         filaProveedor[3] = unProveedor.getTelefonoProveedor();
         filaProveedor[4] = unProveedor.getDireccionProveedor();
          ((DefaultTableModel)TBLProveedor.getModel()).addRow(filaProveedor);
 }
}
 
